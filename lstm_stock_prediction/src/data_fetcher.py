import yfinance as yf
import pandas as pd
from ta import add_all_ta_features
from ta.utils import dropna


def fetch_stock_data(ticker="AAPL", period="5y", interval="1d"):
    """
    Fetch historical stock data from Yahoo Finance.

    Args:
        ticker: Stock symbol (e.g., 'AAPL', 'GOOGL', 'MSFT')
        period: Data period ('1y', '2y', '5y', 'max')
        interval: Data interval ('1d', '1h', '5m')

    Returns:
        DataFrame with OHLCV data and technical indicators

    Data estimates by period (daily interval):
        - '1y'  → ~250 data points  (1 year of trading days)
        - '2y'  → ~500 data points  (2 years of trading days)
        - '5y'  → ~1,250 data points (5 years of trading days)
        - 'max' → all available history (varies by ticker)

    Note:
        US stock market has ~250 trading days per year
        (excludes weekends and public holidays).
        More data gives the LSTM more patterns to learn from,
        improving prediction accuracy.
    """
    # Download OHLCV data (Open, High, Low, Close, Volume) from Yahoo Finance
    df = yf.download(ticker, period=period, interval=interval)

    # yfinance returns MultiIndex columns when downloading a single ticker
    # e.g., ('Close', 'AAPL') — we flatten it to just 'Close'
    df = df.droplevel('Ticker', axis=1) if isinstance(df.columns, pd.MultiIndex) else df

    # Remove any rows with missing values to ensure clean data
    df.dropna(inplace=True)
    return df


def add_technical_indicators(df):
    """
    Add technical indicators (RSI, MACD, Bollinger Bands, etc.)

    Uses the 'ta' (Technical Analysis) library to generate ~80+
    indicators from OHLCV data. These indicators are commonly used
    by traders to identify trends, momentum, volatility, and volume patterns.

    Args:
        df: DataFrame with OHLCV columns

    Returns:
        DataFrame with added technical indicator columns

    Indicators include:
        - Trend: MACD, SMA, EMA, Ichimoku, ADX
        - Momentum: RSI, Stochastic, Williams %R, ROC
        - Volatility: Bollinger Bands, ATR, Keltner Channel
        - Volume: OBV, VWAP, Accumulation/Distribution
    """
    # Remove any NaN rows before calculating indicators
    df = dropna(df)

    # Add all available technical indicators from the 'ta' library
    # fillna=True replaces NaN values generated by indicator calculations
    # (e.g., moving averages need a warmup period before producing values)
    df = add_all_ta_features(
        df,
        open="Open",
        high="High",
        low="Low",
        close="Close",
        volume="Volume",
        fillna=True
    )
    return df


def fetch_realtime_data(ticker="AAPL"):
    """
    Fetch the latest intraday data for real-time demo.

    Pulls 1-minute interval data for the current trading day.
    Data has a ~15 minute delay from actual market prices
    (Yahoo Finance free tier limitation).

    During market hours (9:30 AM - 4:00 PM EST):
        - Returns ~390 data points (1 per minute for 6.5 hours)
    Outside market hours:
        - Returns data from the last trading day

    Args:
        ticker: Stock symbol

    Returns:
        DataFrame with latest intraday data
    """
    # Download 1-minute interval data for the current/last trading day
    df = yf.download(ticker, period="1d", interval="1m")

    # Flatten MultiIndex columns (same as fetch_stock_data)
    df = df.droplevel('Ticker', axis=1) if isinstance(df.columns, pd.MultiIndex) else df

    # Remove any rows with missing values
    df.dropna(inplace=True)
    return df